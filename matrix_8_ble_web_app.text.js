function normalizeTextForFont(text) {
  return text
    .toLocaleUpperCase("tr-TR")
    .replace(/Ğ/g, "G")
    .replace(/Ü/g, "U")
    .replace(/Ş/g, "S")
    .replace(/İ/g, "I")
    .replace(/I/g, "I")
    .replace(/Ö/g, "O")
    .replace(/Ç/g, "C")
    .replace(/[^ A-Z0-9!?.,-]/g, "?");
}

function glyphToColumns(glyphRows) {
  const cols = [];
  for (let c = 0; c < 5; c++) {
    let colBits = 0;
    for (let r = 0; r < 7; r++) {
      if (glyphRows[r][c] === "1") {
        colBits |= (1 << r);
      }
    }
    cols.push(colBits);
  }
  return cols;
}

function columnsToRows(columns) {
  const rows = Array(8).fill(0);
  for (let c = 0; c < 8; c++) {
    const colBits = columns[c] || 0;
    for (let r = 0; r < 8; r++) {
      if ((colBits >> r) & 1) {
        rows[r] |= (1 << (7 - c));
      }
    }
  }
  return rows;
}

function rotateRowsCCW(rows) {
  const out = Array(8).fill(0);
  for (let r = 0; r < 8; r++) {
    for (let c = 0; c < 8; c++) {
      if (((rows[r] >> (7 - c)) & 1) !== 1) continue;
      const nr = 7 - c;
      const nc = r;
      out[nr] |= (1 << (7 - nc));
    }
  }
  return out;
}

export function buildTextScrollFrames(rawText, speedMs, scrollRight, rotate90, brightnessOverride = null) {
  const text = normalizeTextForFont(rawText);
  const columns = [0, 0, 0, 0, 0, 0, 0, 0];
  for (const ch of text) {
    const glyph = FONT_5X7[ch] || FONT_5X7["?"];
    columns.push(...glyphToColumns(glyph), 0);
  }
  columns.push(0, 0, 0, 0, 0, 0, 0, 0);

  const brightness = clamp(brightnessOverride ?? ui.brightnessRange.value, 0, 15, 8);
  const framesOut = [];
  for (let i = 0; i <= columns.length - 8; i++) {
    let rows = columnsToRows(columns.slice(i, i + 8));
    if (rotate90) rows = rotateRowsCCW(rows);
    framesOut.push({
      rows,
      duration: speedMs,
      brightness,
    });
  }

  if (scrollRight) framesOut.reverse();
  if (framesOut.length > MAX_FRAMES) {
    return {
      frames: framesOut.slice(0, MAX_FRAMES),
      truncated: true,
    };
  }

  return {
    frames: framesOut,
    truncated: false,
  };
}
